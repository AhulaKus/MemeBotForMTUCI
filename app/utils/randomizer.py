import random  # Импортируем модуль для работы со случайными числами


def random_token():
    """
    Генерирует случайный токен длиной 10 символов.
    Токен состоит из букв (верхнего и нижнего регистра) и цифр,
    и начинается с символа решетки (#).

    Возвращает:
        Строка случайного токена, например '#aB3dE9FgH'.
    """
    chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789'  # Набор доступных символов
    return '#' + ''.join(random.choices(chars, k=10))  # Генерация токена и добавление решетки в начале
